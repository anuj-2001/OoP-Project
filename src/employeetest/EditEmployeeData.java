    public void populateArrayLists(){
    
        try{
            FileInputStream file = new FileInputStream("Jobs.dat"); 
            ObjectInputStream inputFile2 = new ObjectInputStream(file);
            boolean endOfFile = false;
            while(!endOfFile){
                try{
                    jobs.add((Job)inputFile2.readObject());
                }
                catch(EOFException e){
                    endOfFile = true;
                }
                catch(Exception f){
                    JOptionPane.showMessageDialog(null,f.getMessage());
                }
            }
            inputFile2.close();
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
        try{
            FileInputStream file = new FileInputStream("Employees.dat"); 
            ObjectInputStream inputFile = new ObjectInputStream(file);
            boolean endOfFile = false;
            while(!endOfFile){
                try{
                    employee.add((Employee)inputFile.readObject());
                }
                catch(EOFException e){
                    endOfFile = true;
                }
                catch(Exception f){
                    JOptionPane.showMessageDialog(null,f.getMessage());
                }
            }
            inputFile.close();
        }
        catch(IOException e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
        
    }
    
    public void saveEmployeesToFile(){
        try{
            FileOutputStream file = new FileOutputStream("Employees.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for(int i=0;i<employee.size();i++){
                outputFile.writeObject(employee.get(i));
            }
            outputFile.close();
            JOptionPane.showMessageDialog(null, "Succesfully Saved!!");
            this.dispose();
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }
    public void deleteEmployeesFromFile(){
        try{
            FileOutputStream file = new FileOutputStream("Employees.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            for(int i=0;i<employee.size();i++){
                outputFile.writeObject(employee.get(i));
            }
            outputFile.close();
            JOptionPane.showMessageDialog(null, "Succesfully deleted!!");
            this.dispose();
        }
        catch(IOException e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }

