    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        int selectedIndex = jComboBox1.getSelectedIndex();
        
        jTextField1.setText(employee.get(selectedIndex).getName());
        jTextField2.setText(employee.get(selectedIndex).getSurname());
        jTextField4.setText(employee.get(selectedIndex).getStaffNo()+"");
        
        Job job = employee.get(selectedIndex).getJob();
        int index = 0;
        for(int i=0;i<jobs.size();i++){
            if(jobs.get(i).equals(job)){
                index = i;
                break;
            }
        }
        jComboBox2.setSelectedIndex(index);
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty() || jTextField4.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill all the fields!");
        }
        else {
           int selectedIndex = jComboBox1.getSelectedIndex();
           employee.get(selectedIndex).setName(jTextField1.getText().trim());
           employee.get(selectedIndex).setSurname(jTextField2.getText().trim());
           employee.get(selectedIndex).setStaffNo(Integer.parseInt(jTextField4.getText().trim()));
           Job job = jobs.get(jComboBox2.getSelectedIndex());
           employee.get(selectedIndex).setJob(job);
           saveEmployeesToFile();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       int selectedIndex = jComboBox1.getSelectedIndex();
       employee.remove(selectedIndex);
       deleteEmployeesFromFile();
    }//GEN-LAST:event_jButton2ActionPerformed

